package Cursors;

class Element {
    // Объявляем 2 переменные для хранения самого значения и ссылки на следующий элемент
	public Post values;
	public Position next;
	

	public Element(Post values, Position p){
		// Записываем передаваемые значения в наши переменные
		this.values = values;
		next.p = p.p;
	}
}